    public MemberInfoUI(Member thisM) {
        this.member = thisM;
        try {
            hist = new HistoryManager();
            var histProc = new HistoryProcessor();
            initComponents();
            
            name.setText(thisM.name);
            gender.setText(thisM.getGender());
            currRank.setText(thisM.getTitle());
            joinDate.setText(thisM.getJoinDate().toString());
            pfp.setIcon(new ImageIcon(new ImageIcon(new URL(thisM.getPfp())).getImage().getScaledInstance(pfp.getWidth(), pfp.getHeight(), Image.SCALE_SMOOTH)));
            
            var ranks = new HashSet<String>();
            hist.searchLogs("", "changÃ© le rang de " + member.name, "FR", hist.searchLogs("", member.name, "FR", "default")).forEach(x -> {
                var pat = histProc.getPattern("rank change").matcher(x[1]);
                pat.matches();
                ranks.add(pat.group("title"));
            });
            ranksHad.setText(ranks.toString().substring(1, ranks.toString().length() - 1));
        } catch (ParserConfigurationException | SAXException ex) {
            System.out.println(ex);
        } catch (IOException ioe) {
            if (ioe instanceof java.net.MalformedURLException) {
                System.out.println("No profile picture!");
                // TODO: remove the print statement and set a default profile picture to ones who doesn't have a profile picture.
            }
        }
    }

